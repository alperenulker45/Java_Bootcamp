package Odev19Subat;

public class _06_OdevSoru6 {
    public static void main(String[] args) {
        //6-# **do while loop kullanınız.**
        // "Hello World" yazdırın, i 10 dan küçüktür ve i'yi arttırın. (1 i, 2 i, 3 i, 4 i...)

        int i=0;

        do {
            System.out.println("Hello World");
            i++;
        }
        while (i<10);
    }
}
