package Odev19Subat;

public class _07_OdevSoru7 {
    public static void main(String[] args) {
        //7-# **do while loop kullanınız.**
        // "Hello World" yazdırın, i 0'dan büyüktür ve i'yi azaltın. (9 i, 8 i, 7 i, 6 i...)
        int i=10;

        do {
            System.out.println("Hello World");
            i--;
        }
        while (i>0);
    }
}
