package Gun02;

public class _02_Ornek_1 {
    public static void main(String[] args) {
        System.out.print("Hello World");

//        printin sonundaki "ln" yani line satir yazdiktan sonra alt satira gecilmesini sagliyor.

        System.out.println("Welcome to Java");
        System.out.println("Welcome to \n JAVA");

//        \n ile \n den sonrakileri alt satira yazmasini sagliyoruz.
//        Pazartesi kelimesini her harfi ayri satira gelecek sekilde yaziniz
        System.out.println("P\nA\nZ\nA\nR\nT\nE\nS\nI");

        System.out.println("S");
        System.out.println("A");
        System.out.println("L");
        System.out.println("I");

      /* Bu yazim seklinde pazartesi ve saliyi ayirmadan her harfi alt alta yazdi. araya bosluk koymak icin
      ayirmak istedimiz yere System.out.println() yapabiliriz
       */

        System.out.println("S");
        System.out.println("A");
        System.out.println();
        System.out.println("L");
        System.out.println("I");


    }
}
