package Gun14;

public class _05_NestedLoop {
    public static void main(String[] args) {
        // *****
        // ****
        // ***
        // **
        // *

        for (int satir=5; satir>0; satir--)
        {
            for (int sutun=1; sutun<=satir; sutun++)
                System.out.print("*");

            System.out.println();

        }
    }
}
